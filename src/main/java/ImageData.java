
public class ImageData {
	int imageID;
	String path;
	
	public ImageData(int imageID, String path) {
		super();
		this.imageID = imageID;
		this.path = path;
	}

	public int getImageID() {
		return imageID;
	}

	public void setImageID(int imageID) {
		this.imageID = imageID;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
	
}
